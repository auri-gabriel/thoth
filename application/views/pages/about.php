
<div class="container my-5">
	<h2 class="mb-4 text-primary fw-bold">About Thoth</h2>
	<div class="row justify-content-center">
		<div class="col-lg-8">
			<p class="lead">Thoth is an open source platform designed to support the entire lifecycle of Systematic Literature Reviews (SLR) and related research projects. Our mission is to make evidence-based research more accessible, transparent, and collaborative for everyone.</p>
			<hr>
			<h4 class="mt-4 mb-2 text-secondary fw-semibold">Key Features</h4>
			<ul class="mb-4">
				<li><strong>Project Management:</strong> Organize, create, and manage multiple SLR projects with ease.</li>
				<li><strong>Collaborative Workflow:</strong> Invite team members, assign roles, and track contributions throughout the review process.</li>
				<li><strong>Planning & Protocol:</strong> Define research questions, criteria, databases, and search strategies in a structured way.</li>
				<li><strong>Study Selection & Quality Assessment:</strong> Import studies, perform selection, and assess quality with built-in tools.</li>
				<li><strong>Data Extraction & Reporting:</strong> Extract relevant data and generate visual reports to support your findings.</li>
				<li><strong>Export & Reproducibility:</strong> Export your project data in multiple formats for publication or further analysis.</li>
			</ul>
			<h4 class="mt-4 mb-2 text-secondary fw-semibold">Why Thoth?</h4>
			<ul class="mb-4">
				<li><strong>Open Source:</strong> Thoth is free to use, modify, and contribute to. We believe in open science and community-driven development.</li>
				<li><strong>User-Centered Design:</strong> Built with researchers in mind, Thoth aims for a clean, intuitive, and modern user experience.</li>
				<li><strong>Transparency:</strong> All project actions are logged, ensuring traceability and reproducibility of your research.</li>
				<li><strong>Community Support:</strong> Join our community on <a href="https://github.com/ProjetoESE/Thoth" target="_blank">GitHub</a> to report issues, request features, or contribute code.</li>
			</ul>
